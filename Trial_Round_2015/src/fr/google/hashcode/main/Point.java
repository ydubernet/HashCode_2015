package fr.google.hashcode.main;

public class Point {
	
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	final int x;
	private final int y;
	
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}


}
